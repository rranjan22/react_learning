let myVariable = "hey, this is a string";
console.log(myVariable);
//different string methods
console.log(myVariable.length); // Get the length of the string
console.log(myVariable.toUpperCase()); // Convert to uppercase  
console.log(myVariable.toLowerCase()); // Convert to lowercase
console.log(myVariable.indexOf("hey")); // Find the index of a substring
console.log(myVariable.indexOf("i")); // Find the index of a character
console.log(myVariable.lastIndexOf("s")); // Find the last index of a character
console.log(myVariable.slice(0, 3)); // Extract a substring
console.log(myVariable.replace("hey", "hello")); // Replace a substring
console.log(myVariable.split(" ")); // Split the string into an array
console.log(myVariable.trim()); // Remove whitespace from both ends
console.log(myVariable.startsWith("hey")); // Check if the string starts with a substring
console.log(myVariable.endsWith("string")); // Check if the string ends with a substring
console.log(myVariable.includes("is")); // Check if the string contains a substring
console.log(myVariable.charAt(0)); // Get the character at a specific index
console.log(myVariable.concat(" and more text")); // Concatenate another string
